package org.lifeform.credit;

public class Factor {
	private final String name;
	private final double number;
	private final int id;

	public Factor(final String name, final int id, final double num) {
		this.name = name;
		this.id = id;
		this.number = num;
	}

	public int getId() {
		return id;
	}

	public String getName() {
		return name;
	}

	public double getNumber() {
		return number;
	}

}
