package org.lifeform.market;

public class MarketDepthItem {
	private int size;
	private double price;

	public MarketDepthItem(final int size, final double price) {
		this.size = size;
		this.price = price;
	}

	public int getSize() {
		return size;
	}

	public double getPrice() {
		return price;
	}

	public void setSize(final int size) {
		this.size = size;
	}

	public void setPrice(final double price) {
		this.price = price;
	}
}