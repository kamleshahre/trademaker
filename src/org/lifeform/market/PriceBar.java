package org.lifeform.market;

public class PriceBar extends StockQuote {
	public PriceBar(final double price) {
		super(Type.Price, price);
	}

	public PriceBar(final double open, final double high, final double low, final double close,
			final long volume) {
		super(open, high, low, close, volume);
	}

	public PriceBar(final long date, final double open, final double high, final double low,
			final double close, final long volume) {
		super(date, open, high, low, close, volume);
	}
}
