import java.util.Hashtable;

public class ProductIdentity extends Hashtable<String, Object> {
	private static final long serialVersionUID = 8751278753477351865L;

	private final int id;

	public ProductIdentity(final int id) {
		super();
		this.id = id;
	}

	public int getId() {
		return id;
	}
}
